var SubCategory = function(id, categoryID, name, code, shortDesc, longDesc){
    this.ID = id;
    this.CategoryID = categoryID;
    this.Name = name;
    this.Code = code;
    this.ShortDesc = shortDesc;
    this.LongDesc = longDesc;
};